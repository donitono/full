-- Function to create a dashboard section for fish data
function CreateFishDataSection(parent)
    local section = Instance.new("Frame", parent)
    section.Size = UDim2.new(1, -10, 0, 320)
    section.Position = UDim2.new(0, 5, 0, 710) -- below existing dashboard sections
    section.BackgroundColor3 = Color3.fromRGB(45,45,52)
    section.BorderSizePixel = 0
    Instance.new("UICorner", section)

    local title = Instance.new("TextLabel", section)
    title.Size = UDim2.new(1, -20, 0, 25)
    title.Position = UDim2.new(0, 10, 0, 5)
    title.Text = "🐟 Fish Data Table"
    title.Font = Enum.Font.GothamBold
    title.TextSize = 14
    title.TextColor3 = Color3.fromRGB(100,200,255)
    title.BackgroundTransparency = 1
    title.TextXAlignment = Enum.TextXAlignment.Left

    -- Column headers
    local headers = {"Name", "Rarity", "Icon", "Sell", "Probability"}
    for i, header in ipairs(headers) do
        local lbl = Instance.new("TextLabel", section)
        lbl.Size = UDim2.new(0.2, -4, 0, 20)
        lbl.Position = UDim2.new((i-1)*0.2, 10, 0, 35)
        lbl.Text = header
        lbl.Font = Enum.Font.GothamBold
        lbl.TextSize = 12
        lbl.TextColor3 = Color3.fromRGB(200,200,200)
        lbl.BackgroundTransparency = 1
        lbl.TextXAlignment = Enum.TextXAlignment.Left
    end

    -- Scrollable list for fish data
    local scroll = Instance.new("ScrollingFrame", section)
    scroll.Size = UDim2.new(1, -20, 1, -60)
    scroll.Position = UDim2.new(0, 10, 0, 60)
    scroll.BackgroundColor3 = Color3.fromRGB(35,35,42)
    scroll.BorderSizePixel = 0
    scroll.ScrollBarThickness = 4
    scroll.ScrollBarImageColor3 = Color3.fromRGB(80,80,80)
    Instance.new("UICorner", scroll)

    local y = 0
    for name, data in pairs(FishData) do
        local row = Instance.new("Frame", scroll)
        row.Size = UDim2.new(1, 0, 0, 22)
        row.Position = UDim2.new(0, 0, 0, y)
        row.BackgroundTransparency = 1

        local nameLbl = Instance.new("TextLabel", row)
        nameLbl.Size = UDim2.new(0.2, -4, 1, 0)
        nameLbl.Position = UDim2.new(0, 0, 0, 0)
        nameLbl.Text = name
        nameLbl.Font = Enum.Font.Gotham
        nameLbl.TextSize = 11
        nameLbl.TextColor3 = Color3.fromRGB(255,255,255)
        nameLbl.BackgroundTransparency = 1
        nameLbl.TextXAlignment = Enum.TextXAlignment.Left

        local rarityLbl = Instance.new("TextLabel", row)
        rarityLbl.Size = UDim2.new(0.2, -4, 1, 0)
        rarityLbl.Position = UDim2.new(0.2, 0, 0, 0)
        rarityLbl.Text = data.rarity
        rarityLbl.Font = Enum.Font.Gotham
        rarityLbl.TextSize = 11
        rarityLbl.TextColor3 = Color3.fromRGB(255,255,255)
        rarityLbl.BackgroundTransparency = 1
        rarityLbl.TextXAlignment = Enum.TextXAlignment.Left

        local iconLbl = Instance.new("ImageLabel", row)
        iconLbl.Size = UDim2.new(0.2, -4, 1, 0)
        iconLbl.Position = UDim2.new(0.4, 0, 0, 0)
        iconLbl.Image = data.icon
        iconLbl.BackgroundTransparency = 1

        local sellLbl = Instance.new("TextLabel", row)
        sellLbl.Size = UDim2.new(0.2, -4, 1, 0)
        sellLbl.Position = UDim2.new(0.6, 0, 0, 0)
        sellLbl.Text = tostring(data.sell)
        sellLbl.Font = Enum.Font.Gotham
        sellLbl.TextSize = 11
        sellLbl.TextColor3 = Color3.fromRGB(255,255,255)
        sellLbl.BackgroundTransparency = 1
        sellLbl.TextXAlignment = Enum.TextXAlignment.Left

        local probLbl = Instance.new("TextLabel", row)
        probLbl.Size = UDim2.new(0.2, -4, 1, 0)
        probLbl.Position = UDim2.new(0.8, 0, 0, 0)
        probLbl.Text = tostring(data.probability)
        probLbl.Font = Enum.Font.Gotham
        probLbl.TextSize = 11
        probLbl.TextColor3 = Color3.fromRGB(255,255,255)
        probLbl.BackgroundTransparency = 1
        probLbl.TextXAlignment = Enum.TextXAlignment.Left

        y = y + 24
    end
    scroll.CanvasSize = UDim2.new(0, 0, 0, y)

    return section
end

